    "- Company profile/multiples → get_company (includes key metrics)\n"
    "- Detailed valuation metrics → get_metrics\n"
    "- Revenue, net income, EPS trends → get_financials\n"
    "- PE, PB, valuation assessment → get_valuation\n"
    "- Analyst price targets, EPS estimates → get_analyst_consensus\n"
    "- Earnings call sentiment trends → get_transcript_sentiment\n"
    "- 8-K material events → get_events\n"
    "- Composite signals → get_signals\n"
    "- Price/volume anomalies → get_anomalies\n"
    "- Stock price, returns, volatility → get_prices\n"
    "- News headlines → get_news\n"
    "- Insider buy/sell activity → get_insider\n"
    "- Macro environment → get_macro\n"
    "- Find companies by criteria → screen_companies\n"
    "- Compare companies → call tools for each, then synthesize\n\n"
    "## OUTPUT FORMAT\n"
    "1. Lead with the direct answer and key numbers (with citations).\n"
    "2. Follow with supporting analysis: trends, comparisons, context.\n"
    "3. End with caveats or data limitations if relevant.\n"
    "Keep responses focused and quantitative. Avoid generic commentary "
    "that doesn't reference specific data from the tools."
